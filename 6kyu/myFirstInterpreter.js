function myFirstInterpreter(code) {
  let memory = 0;
  let result = "";
  for (let c of [...code]) {
    if (c === '+') {
      memory = (++memory) % 256;
    } else if (c == '.') {
      result += String.fromCharCode(memory);
    }
  }
  return result;
}

